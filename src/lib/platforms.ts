/** All platforms sorted alphabetically by vendor, then by system name */
export const PLATFORMS_SORTED: Platform[] = [...PLATFORMS].sort((a, b) => {
  const va = platformVendor(a.slug);
  const vb = platformVendor(b.slug);
  return va === vb ? a.name.localeCompare(b.name) : va.localeCompare(vb);
});

/** Slugs of folder-based platforms (an extracted Wii U title, etc.) — a ROM is
 *  a directory, not a single file. Callers that stream file bytes (hashing)
 *  must skip these. */
export const FOLDER_ROM_SLUGS: string[] = PLATFORMS.filter((p) => p.folderRom).map((p) => p.slug);

const bySlug = new Map(PLATFORMS.map((p) => [p.slug, p]));
const byExtension = new Map<string, Platform>();
for (const p of PLATFORMS) for (const ext of p.extensions) byExtension.set(ext, p);
// libretroName is exactly the libretro-database DAT filename (sans .dat), so it
// maps DAT files onto our slugs directly. Several platforms share a name (e.g.
// two arcade entries) — first definition wins.
const byLibretroName = new Map<string, Platform>();
for (const p of PLATFORMS) if (!byLibretroName.has(p.libretroName)) byLibretroName.set(p.libretroName, p);

export function platformBySlug(slug: string): Platform | undefined {
  return bySlug.get(slug);
}

export function platformByExtension(ext: string): Platform | undefined {
  return byExtension.get(ext.toLowerCase());
}

/** Resolve a platform from a folder name (e.g. "SNES", "Mega Drive") */
export function platformByFolder(folderName: string): Platform | undefined {
  const needle = folderName.toLowerCase().trim();
  return PLATFORMS.find(
    (p) => p.folderAliases.includes(needle) || p.name.toLowerCase() === needle
  );
}

/** Resolve a platform from a libretro-database system name (== DAT filename
 *  without the .dat extension, e.g. "Nintendo - Nintendo Entertainment System") */
export function platformByLibretroName(name: string): Platform | undefined {
  return byLibretroName.get(name.trim());
}
