// Platform registry: maps file extensions and folder names to platforms,
// emulator (libretro) cores, and libretro-thumbnails system names.

export interface Platform {
  slug: string;
  name: string;
  shortName: string;
  /** File extensions (lowercase, with dot) that uniquely identify this platform */
  extensions: string[];
  /** Folder-name aliases (lowercase) used to resolve ambiguous extensions like .zip/.bin/.iso */
  folderAliases: string[];
  /** EmulatorJS core id used to play this system in-browser; null = not
   *  emulated (e.g. Flash, which plays via Ruffle) */
  ejsCore: string | null;
  /** System directory name on thumbnails.libretro.com */
  libretroName: string;
  /** Accent color used for platform badges / placeholder art */
  color: string;
  /**
   * Folder-based games (e.g. an extracted Wii U title). A directory that
   * contains ALL of these child entries is treated as a single ROM — the
   * scanner records the folder itself and does not descend into it.
   */
  folderRom?: { requires: string[] };
}

